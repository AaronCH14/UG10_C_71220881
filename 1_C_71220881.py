print("============Pilih Menu=============")
print("1. Tambah")
print("2. Kurang")
print("3. Kali")
print("4. Bagi")
angkapertama=int(input("Masukkan angka pertama:"))
angkakedua=int(input("Masukkan angka kedua :"))
Menu= input("Masukkan pilihan:")
if Menu=="1" :
    Tambah= angkapertama+angkakedua
    print (Tambah)
elif Menu=="2" :
    Kurang= angkapertama-angkakedua
    print (Kurang)
elif Menu=="3" :
    Kali= angkapertama*angkakedua
    print (Kali)
elif Menu=="4" :
    Bagi= angkapertama/angkakedua
    print (Bagi)
    

    

      
